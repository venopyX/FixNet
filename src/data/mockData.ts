// Base interface for all models
export interface BaseModel {
  id: string
  created_at: string
  updated_at: string
}

// Enhanced User interface with all new fields
export interface User extends BaseModel {
  first_name: string
  last_name: string
  email: string
  password: string
  role: 'resident' | 'agency_staff' | 'admin' | 'super_admin'
  status: 'active' | 'inactive' | 'suspended'
  contact_enabled?: boolean
  agency?: string
  assigned_categories?: string[]
  last_login?: string
  permissions?: string[]
}

// Enhanced Report interface with all new fields
export interface Report extends BaseModel {
  resident_id: string
  title: string
  description: string
  category: 'roads' | 'electricity' | 'water' | 'waste' | 'safety' | 'other'
  location: string
  photo_url?: string
  status: 'pending' | 'under_review' | 'resolved' | 'rejected'
  longitude?: number
  latitude?: number
  priority: 'low' | 'medium' | 'high'
  assigned_to?: string
  resolution_comment?: string
  estimated_completion?: string
  actual_completion?: string
}

// Enhanced StatusUpdate interface
export interface StatusUpdate extends BaseModel {
  report_id: string
  admin_id?: string
  previous_status?: string
  new_status: string
  admin_comment?: string
  updated_by?: string
  is_public: boolean
  attachment_url?: string
}

// New Notification interface
export interface Notification extends BaseModel {
  user_id: string
  type: 'new_report' | 'status_update' | 'assignment' | 'high_priority' | 'reminder'
  title: string
  message: string
  is_read: boolean
  related_report_id?: string
  metadata?: Record<string, any>
}

// New Category interface
export interface Category extends BaseModel {
  name: string
  display_name: string
  description: string
  icon: string
  color: string
  is_active: boolean
  assigned_agency?: string
  estimated_response_time: number
}

